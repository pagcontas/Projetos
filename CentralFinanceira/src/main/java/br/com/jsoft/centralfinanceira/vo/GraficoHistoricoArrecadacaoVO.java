package br.com.jsoft.centralfinanceira.vo;

import java.math.BigDecimal;

/**
 *
 * @author Juniel
 */
public class GraficoHistoricoArrecadacaoVO {
    
    private Integer periodo;
    
    private BigDecimal boletos;
    
    private BigDecimal site;
    
    private BigDecimal creditos;
    
    private BigDecimal recargas;
    
    private BigDecimal ops;
    
    private BigDecimal valegas;
    
    private BigDecimal bb;
    
    private BigDecimal bp;

    public Integer getPeriodo() {
        return periodo;
    }

    public void setPeriodo(Integer periodo) {
        this.periodo = periodo;
    }

    public BigDecimal getBoletos() {
        return boletos;
    }

    public void setBoletos(BigDecimal boletos) {
        this.boletos = boletos;
    }

    public BigDecimal getSite() {
        return site;
    }

    public void setSite(BigDecimal site) {
        this.site = site;
    }

    public BigDecimal getCreditos() {
        return creditos;
    }

    public void setCreditos(BigDecimal creditos) {
        this.creditos = creditos;
    }

    public BigDecimal getRecargas() {
        return recargas;
    }

    public void setRecargas(BigDecimal recargas) {
        this.recargas = recargas;
    }

    public BigDecimal getOps() {
        return ops;
    }

    public void setOps(BigDecimal ops) {
        this.ops = ops;
    }

    public BigDecimal getValegas() {
        return valegas;
    }

    public void setValegas(BigDecimal valegas) {
        this.valegas = valegas;
    }

    public BigDecimal getBb() {
        return bb;
    }

    public void setBb(BigDecimal bb) {
        this.bb = bb;
    }

    public BigDecimal getBp() {
        return bp;
    }

    public void setBp(BigDecimal bp) {
        this.bp = bp;
    }
    
    
    
}
